#include "main.h"

/**
* puts2 - prints every other character of a string, starting with the first 
* character, followed by a new line
* @str: pointer to a string value
*/

void puts2(char *str)
{
    int i, str;
    
    for (i >= 0; i <= str; i++)
    {
        while (str != '\0')
        {
            putchar(str);
        }
    }
    putchar('\n');
}
